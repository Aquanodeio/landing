// Fallback component
function GlobeFallback() {
  return (
    <div className="flex items-center justify-center h-[500px] bg-black/80 rounded-lg border border-gray-800">
      <div className="text-center">
        <div className="text-xl text-blue-400 mb-2">Global Network</div>
        <p className="text-zinc-400 text-md max-w-md mx-auto">
          Connect and transact seamlessly with users across the globe
        </p>
      </div>
    </div>
  );
}

export default function GlobeDisplay() {
  const [hasError, setHasError] = useState(false);

  if (hasError) {
    return <GlobeFallback />;
  }

  return (
    <div className="relative w-full h-[500px] my-16 bg-black">
      {/* Globe container */}
      <div className="absolute inset-0 w-full h-full">
        <ErrorBoundary onError={() => setHasError(true)}>
          <World globeConfig={globeConfig} data={sampleArcs} />
        </ErrorBoundary>
      </div>

      {/* Footer gradient */}
      <div className="absolute bottom-0 left-0 right-0 h-20 bg-gradient-to-b from-transparent to-black pointer-events-none z-10" />
    </div>
  );
}

// Simple error boundary component
class ErrorBoundary extends React.Component<
  { children: React.ReactNode; onError: () => void },
  { hasError: boolean }
> {
  constructor(props: { children: React.ReactNode; onError: () => void }) {
    super(props);
    this.state = { hasError: false };
  }

  static getDerivedStateFromError() {
    return { hasError: true };
  }

  componentDidCatch(error: Error) {
    console.error("Globe rendering error:", error);
    this.props.onError();
  }

  render() {
    if (this.state.hasError) {
      return <GlobeFallback />;
    }
    return this.props.children;
  }
}
